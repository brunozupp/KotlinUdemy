package standardfunctions

/**
 * Standard Functions are basically functions that are applied in objects that will take a lambda expression and will
 * give me some result in the context of that object.
 * Examples of this type of function are: let, with, run, apply, also, takeIf and takeUnless
 *
 * ------------------------------------
 *
 * Also called scope functions
 * Execute code in the context of an object
 * Creates a temporary scope while the code is executed
 * In the scope, you can access the original object
 *
 * There are 5 scope functions, and 2 additional 'check' functions
 * Scope functions: let, with, run, apply and also
 * Check functions: takeIf and takeUnless
 */